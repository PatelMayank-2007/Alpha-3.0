import java.util.*;
public class _01_Bitwise_operator {
    public static void main(String[] args) {
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~5);
        System.out.println(~0);
        System.out.println(5<<2);
        System.out.println(7>>1);
    }
}
